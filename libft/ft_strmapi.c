#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		size;
	char	*p;

	i = 0;
	if (!s || !f)
		return (NULL);
	size = ft_strlen(s);
	p = malloc(sizeof(char) * size + 1);
	if (!p)
		return (NULL);
	while (s[i] != '\0')
	{
		p[i] = f(i, s[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
}
